#include <stdio.h>
#include "push_swap.h"

void	check_minus_signs(char **s1, char **s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if ((*s1)[i] == '-' && (*s2)[j] == '-')
	{
		(*s1)++;
		(*s2)++;
	}
}
